# ── Маппинг intent → промпт ───────────────────────────────────────────────────

INTENT_PROMPTS: dict[str, str] = {
    "products":      PRODUCTS_PROMPT,
    "catalog_browse": PRODUCTS_PROMPT,
    "compare":       COMPARE_PROMPT,
    "info":          INFO_PROMPT,
    "order_help":    ORDER_PROMPT,
    "promotions":    PROMOTIONS_PROMPT,
    "multi":         MULTI_PROMPT,
}


def get_prompt_for_intent(intent: str) -> str:
    """Вернуть системный промпт для указанного intent-а."""
    return INTENT_PROMPTS.get(intent, MULTI_PROMPT)


